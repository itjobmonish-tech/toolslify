export function getToolConfig(slug) {
  return TOOL_CONFIGS[slug] || null;
}

export function getAllToolConfigs() {
  return TOOL_SLUGS.map((slug) => TOOL_CONFIGS[slug]);
}

export function getRelatedToolConfigs(currentSlug) {
  const preferred = [
    "ai-email-writer",
    "resume-bullet-generator",
    "cover-letter-generator",
    "instagram-caption-generator",
    "tweet-rewriter",
  ];

  return preferred
    .filter((slug) => slug !== currentSlug)
    .map((slug) => TOOL_CONFIGS[slug])
    .filter(Boolean);
}
