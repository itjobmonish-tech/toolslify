export function createSeoFaqSchema(page) {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    name: page.title,
    mainEntity: page.faq.map((item) => ({
      "@type": "Question",
      name: item.question,
      acceptedAnswer: {
        "@type": "Answer",
        text: item.answer,
      },
    })),
  };
}
