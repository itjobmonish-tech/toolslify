export function getUIText(language) {
  return {
    ...ENGLISH_UI_TEXT,
    ...(UI_TEXT[language] || {}),
  };
}

export function formatUiText(template, replacements = {}) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) => {
    const value = replacements[key];
    return value === undefined || value === null ? "" : String(value);
  });
}
